              <p>
              Al momento de efectuar una óptima homologación de los 
              proveedores es preciso seguir una serie de pasos para garantizar que se valoran todos los aspectos necesarios.      

              </p>
              <ul class="box-pricing-list">
                        <li class="active">Hacer una selección de proveedores. Lo primordial es tener claro a qué proveedores se va a 
                          evaluar para ver si pueden colaborar con la compañía. 
                          Con esta selección se debe hacer un análisis previamente sabiendo de antemano qué necesidades tiene cada proveedor y poder así catalogarlos.</li>
                        <li class="active">Pasar un cuestionario de homologación. Se tendrán que elaborar los aspectos que se quiere evaluar para ver si 
                          estos proveedores se asemejan a los que se necesita: 
                          periocidad de pago, calidad del material, seriedad, responsabilidad… todo aquello que se considere importante para valorar el proveedor.</li>
                        <li class="active">Evaluar las respuestas de los proveedores. Hay que verificar qué respuestas han brindado los proveedores y 
                          valorarles en función al estándar que se haya determinado en la compañía.</li>
                        <li class="active">En el caso que se decida por visitar a la empresa, éste es el mejor momento para realizarla. 
                          En ella se verificará en primera instancia las instalaciones del proveedor y si éstas son lo que requiere la compañía.</li>
                        <li class="active">Emitir el reporte de homologación. Una vez finalizada la visita y evaluado todos los resultados, 
                          hay que efectuar un reporte de homologación que determine si el proveedor es admisible o no.</li>

              </ul>
              <p>
              Ya sea por un método o por otro, gracias a la homologación se conseguirá un listado de proveedores que pueden trabajar con la corporación, 
